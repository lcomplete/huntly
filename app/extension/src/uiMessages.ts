export type TranslationKey = keyof typeof UI_MESSAGES.en;

export function formatMessage(
  template: string,
  values?: TranslationValues
): string {
  if (!values) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = values[key];
    return value === undefined ? `{${key}}` : String(value);
  });
}

export function translateUi(
  language: DisplayLanguage,
  key: TranslationKey,
  values?: TranslationValues
): string {
  const currentMessages = UI_MESSAGES[language] || UI_MESSAGES.en;
  return formatMessage(currentMessages[key] || UI_MESSAGES.en[key] || key, values);
}
